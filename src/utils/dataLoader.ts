// 从public/commands目录动态获取命令列表
export async function fetchCommandList(): Promise<string[]> {
  try {
    // 在测试环境中，返回一些基本命令用于测试
    // if (process.env.NODE_ENV === 'test') {
    //   cachedCommandList = ALL_COMMANDS.slice(0, 10)
    //   return cachedCommandList
    // }

    // 直接使用硬编码的完整命令列表，这是最可靠的方式
    // 因为我们无法通过HTTP请求获取目录内容
    console.log(`Using complete command list with ${ALL_COMMANDS.length} commands`);

    // 缓存结果
    cachedCommandList = ALL_COMMANDS;
    return ALL_COMMANDS;
  } catch (error) {
    console.error('Error in fetchCommandList:', error);
    // 失败时仍返回完整命令列表
    cachedCommandList = ALL_COMMANDS;
    return ALL_COMMANDS;
  }
}

// 获取命令列表（同步接口，用于搜索等场景）
export function getCommandList(): string[] {
  // 如果缓存未加载，立即触发异步加载并返回完整命令列表
  if (!cachedCommandList) {
    // 触发异步加载，但不等待其完成
    fetchCommandList().catch(console.error);
    return ALL_COMMANDS;
  }
  return cachedCommandList;
}

// 预加载命令列表
export async function preloadCommandList(): Promise<void> {
  await fetchCommandList();
}

// 加载命令详情
export async function loadCommand(commandName: string): Promise<CommandInfo | null> {
  try {
    // 在测试环境中，直接返回模拟数据避免URL解析错误
    // if (process.env.NODE_ENV === 'test') {
    //   return {
    //     name: commandName,
    //     description: `${commandName}命令的描述`,
    //     html: `<h1>${commandName}</h1><p>${commandName}命令的描述</p>`
    //   }
    // }

    // 构建正确的URL路径 - 使用public/commands目录
    const url = `/commands/${commandName}.md`;
    const response = await fetch(url);

    if (!response.ok) {
      return null;
    }

    const content = await response.text();
    return await parseMarkdown(content);
  } catch (error) {
    console.error(`Error loading command ${commandName}:`, error);
    return null;
  }
}

// 搜索命令
export function searchCommands(query: string): string[] {
  // 使用当前可用的命令列表
  const commands = getCommandList();

  if (!query) {
    return commands;
  }

  const lowercaseQuery = query.toLowerCase();
  return commands.filter((cmd) => cmd.toLowerCase().includes(lowercaseQuery));
}
